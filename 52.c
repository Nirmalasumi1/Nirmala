#include<stdio.h>
int main()
{
int n;
printf("Enter the number:");
scanf("%d",&n);
switch(n)
{
case0:
printf("zero");
break;
case1:
printf("one");
break;
case2:
printf("Two");
break;
case3:
printf("Three");
break;
case4:
printf("Four");
break;
case5:
printf("Five");
break;
case6:
printf("Six");
break;
case7:
printf("Seven");
break;
case8:
printf("Eight");
break;
case9:
printf("Nine");
break;
default:
    printf("Enter the number:");
}
return 0;
}
